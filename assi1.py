# Integer data type
integer_num = 10

# Floating-point data type
float_num = 3.14

# Complex data type
complex_num = 2 + 3j

# Addition
add_result = integer_num + float_num

# Subtraction
sub_result = float_num - integer_num

# Multiplication
mul_result = integer_num * complex_num

# Division
div_result = float_num / integer_num

# Exponentiation
exp_result = integer_num ** 2

# Print results
print("Integer:", integer_num)
print("Float:", float_num)
print("Complex:", complex_num)
print("Addition Result:", add_result)
print("Subtraction Result:", sub_result)
print("Multiplication Result:", mul_result)
print("Division Result:", div_result)
print("Exponentiation Result:", exp_result)
