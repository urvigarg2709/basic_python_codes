# Boolean values
a = True
b = False

# Logical NOT operator
not_a = not a
not_b = not b

# Logical AND operator
and_result = a and b

# Logical OR operator
or_result = a or b

# Logical XOR operator
xor_result = a != b

# Print results
print("a:", a)
print("b:", b)
print("NOT a:", not_a)
print("NOT b:", not_b)
print("a AND b:", and_result)
print("a OR b:", or_result)
print("a XOR b:", xor_result)
